    def load_mask(self, image_id):
        info = self.image_info[image_id]
        
        mask = np.zeros((IMAGE_SIZE, IMAGE_SIZE, len(info['annotations'])), dtype=np.uint8)
        labels = []
        
        for m, (annotation, label) in enumerate(zip(info['annotations'], info['labels'])):
            sub_mask = np.zeros((info['height'], info['width']), dtype=np.uint8)
            annotation = ast.literal_eval(annotation)
            # annotation = [int(x) for x in annotation[0].split(' ') if x != '']

            points = []            
            for i in range(len(annotation) - 1):
                if i * 2 + 1 >= len(annotation):
                    break
                points.append((annotation[i*2], annotation[i*2+1]))
                sub_mask[annotation[i*2+1], annotation[i*2]] = 1
            
            points = np.asarray(points)
            cv2.fillPoly(sub_mask, [points], (255,))
            sub_mask = cv2.resize(sub_mask, (IMAGE_SIZE, IMAGE_SIZE), interpolation=cv2.INTER_NEAREST)
            
            mask[:, :, m] = sub_mask
            # labels.append(int(label)+1)
            labels.append(1)
        return mask, np.array(labels)